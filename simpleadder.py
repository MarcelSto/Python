#Start - Simple Adder 

#Input first Numbers
number1 = float(input("Enter the first number: "))
number2 = float(input("Enter the second number: "))

#add Number 1 and Number 2
result = number1 + number2

#Output result
print("The result is:", result)


#input(...): Holt die Eingabe vom Benutzer als Text (String).
#float(...): Wandelt die Eingabe in eine Gleitkommazahl um (funktioniert auch mit Ganzzahlen).
#print(...): Gibt das Ergebnis aus.